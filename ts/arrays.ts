// Vou criar um exemplo utilizando arrays para armazenar uma lista de números

// Criando um array de números
const numbersArray: number[] = [1, 2, 3, 4, 5];

// Imprimindo o array no console
console.log('Array de números:', numbersArray);

// Acessando elementos individuais do array e imprimindo no console
console.log('Primeiro elemento:', numbersArray[0]); // O primeiro elemento do array
console.log('Último elemento:', numbersArray[numbersArray.length - 1]); // O último elemento do array
