// Vou criar um exemplo simples utilizando condicionais para determinar o estado de um número

// Definindo uma variável para o número
const number: number = 10;

// Usando condicionais para verificar o estado do número
if (number > 0) {
  // Se o número for maior que 0, imprimo uma mensagem informando que é positivo
  console.log('O número é positivo.');
} else if (number < 0) {
  // Se o número for menor que 0, imprimo uma mensagem informando que é negativo
  console.log('O número é negativo.');
} else {
  // Se o número não for maior nem menor que 0, então é 0
  console.log('O número é zero.');
}
